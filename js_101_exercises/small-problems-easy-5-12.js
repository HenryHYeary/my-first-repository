/*
input: a string argument representing the time of day.
output: an integer representing how many minutes the input time is after midnight.
As well as a function that returns an integer representing how many minutes the time of day is before midnight.

Examples: both 00:00 and 24:00 should log 0 on the before and after functions.

Data Structure: Should convert the first two and last two numbers (before and after the colon) to separate variables.
Should then convert the string values to numbers.
Should use constant variables for minutes per hour and hours per day as well as minutes per day.

Algorithm: should take the hour value and multiply it by the number of minutes per hour should then add that value to the number of minutes for the after midnight function.
Should repeat the same process as after midnight as before midnight with the key difference being that you want to subtract the value generated by after midnight from the minutes per day constant. 
*/

const MINUTES_PER_HOUR = 60;
const HOURS_PER_DAY = 24;
const MINUTES_PER_DAY = MINUTES_PER_HOUR * HOURS_PER_DAY;

function afterMidnight(time) {
  let hours = time[0] + time[1];
  let minutes = time[3] + time[4];

  let totalMinutes = (Number(hours) * MINUTES_PER_HOUR) + Number(minutes);

  if (time === `${String(HOURS_PER_DAY)}:00`) {
    return 0
  } else {
    return totalMinutes;
  }
}

function beforeMidnight(time) {
  if (time === `${String(HOURS_PER_DAY)}:00` || time === '00:00') {
    return 0;
  } else {
    return MINUTES_PER_DAY - afterMidnight(time);
  }
}

console.log(afterMidnight("00:00") === 0);
console.log(beforeMidnight("00:00") === 0);
console.log(afterMidnight("12:34") === 754);
console.log(beforeMidnight("12:34") === 686);
console.log(afterMidnight("24:00") === 0);
console.log(beforeMidnight("24:00") === 0);